			<ul>
				<li>
					&nbsp;Menu Purchase Order ( PO ).</li>
			</ul>
			<p>
				- Menu ini berisi tabel Daftar dari Purchase Order ( PO ) yang sudah di buat secara detail, seperti : Tanggal, Nomor, Project ID, Suplier, Penerima, dan Gd. tujuan ( Gudang Tujuan ).</p>
			<p>
				<img alt="" src="assets/images/image_8.png"></p>
			<p>
				&nbsp;</p>
			<p>
				- Di bagian kiri atas tampilan, terdapat tulisan &quot;Daftar Purchase Order&quot;.</p>
			<p>
				- Kemudian, di bawahnya terdapat kolom yang berisi drop down :</p>
			<p>
				1. Tampilkan Data : berfungsi untuk membagi halaman dari tampilan data yang ada.</p>
			<p>
				2. Cari : berfungsi untuk mencari data PO berdasarkan no. PO, tanggal, project, gudang tujuan, suplier dan kontak person.</p>
			<p>
				3. Project : berfungsi untuk menngelompokkan berdasarkan project&nbsp;</p>
			<p>
				Langkah akses/pengolahan/pencetakan detail PO:</p>
